ALTER TABLE users ENABLE ROW LEVEL SECURITY;
ALTER TABLE pairing_codes ENABLE ROW LEVEL SECURITY;
ALTER TABLE caregiver_senior_links ENABLE ROW LEVEL SECURITY;
ALTER TABLE medicines ENABLE ROW LEVEL SECURITY;
ALTER TABLE wellbeing_checkins ENABLE ROW LEVEL SECURITY;
ALTER TABLE alerts ENABLE ROW LEVEL SECURITY;

-- Users
CREATE POLICY "users_all" ON users FOR ALL USING (true) WITH CHECK (true);

-- Pairing codes
CREATE POLICY "pairing_codes_all" ON pairing_codes FOR ALL USING (true) WITH CHECK (true);

-- Links
CREATE POLICY "links_all" ON caregiver_senior_links FOR ALL USING (true) WITH CHECK (true);

-- Medicines
CREATE POLICY "medicines_all" ON medicines FOR ALL USING (true) WITH CHECK (true);

-- Wellbeing
CREATE POLICY "wellbeing_all" ON wellbeing_checkins FOR ALL USING (true) WITH CHECK (true);

-- Alerts
CREATE POLICY "alerts_all" ON alerts FOR ALL USING (true) WITH CHECK (true);

-- ============================================================
-- TRIGGER: auto-update updated_at on users
-- ============================================================
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_users_updated_at
  BEFORE UPDATE ON users
  FOR EACH ROW
  EXECUTE FUNCTION update_updated_at();
